function theme_rnf_file_link($variables) {
  $file = $variables['file'];
  //$icon_directory = $variables['icon_directory'];
  $icon_directory = 'sites/default/files/icons';
 
  $url = file_create_url($file->uri);
  $icon = theme('file_icon', array('file' => $file, 'icon_directory' => $icon_directory));

  // Set options as per anchor format described at
  // <a href="http://microformats.org/wiki/file-format-examples" title="http://microformats.org/wiki/file-format-examples">http://microformats.org/wiki/file-format-examples<;/a>
  $options = array(
		   'attributes' => array(
					 'type' => $file->filemime . '; length=' . $file->filesize,
					 ),
		   );
 
  // Use the description as the link text if available.
  if (empty($file->description)) {
    $link_text = $file->filename;
  }
  else {
    $link_text = $file->description;
    $options['attributes']['title'] = check_plain($file->filename);
  }
 
  //open files of particular mime types in new window
  //add mime types to this array if you want more than just PDF files
  $new_window_mimetypes = array('application/pdf','text/plain');
  if (in_array($file->filemime, $new_window_mimetypes)) {
    $options['attributes']['target'] = '_blank';
  }
 
  return '<span class="file">' . $icon . ' ' . l($link_text, $url, $options) . '</span>';
}

function theme_rnf_username($variables) {
  $account = $variables['account'];
  if (isset($variables['link_path'])) {
    $output = l($variables['name'] . $variables['extra'], $variables['link_path'], $variables['link_options']);
  }
  else
    $output = format_username($account);
  return $output;
}

